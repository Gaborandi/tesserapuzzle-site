  Object.keys(EXTENDED_I18N).forEach(function (lang) {
    I18N[lang] = EXTENDED_I18N[lang];
  });

  function normalizeLang(value) {
    var key = (value || "").toLowerCase().split("-")[0];
    return LANGUAGES[key] ? key : "en";
  }

  function getInitialLang() {
    var params = new URLSearchParams(window.location.search);
    var fromUrl = params.get("lang");
    if (fromUrl && LANGUAGES[normalizeLang(fromUrl)]) return normalizeLang(fromUrl);
    try {
      var stored = localStorage.getItem(STORAGE_KEY);
      if (stored && LANGUAGES[normalizeLang(stored)]) return normalizeLang(stored);
    } catch (error) {
      // Ignore private browsing storage errors.
    }
    return normalizeLang(navigator.language || "en");
  }

  function rememberLang(lang) {
    try {
      localStorage.setItem(STORAGE_KEY, lang);
    } catch (error) {
      // Ignore private browsing storage errors.
    }
    var url = new URL(window.location.href);
    if (lang === "en") url.searchParams.delete("lang");
    else url.searchParams.set("lang", lang);
    window.history.replaceState(null, "", url);
  }

  function captureDefaults() {
    document.querySelectorAll("[data-i18n]").forEach(function (el) {
      if (!el.dataset.i18nDefault) el.dataset.i18nDefault = el.textContent;
    });
    document.querySelectorAll("[data-i18n-html]").forEach(function (el) {
      if (!el.dataset.i18nDefaultHtml) el.dataset.i18nDefaultHtml = el.innerHTML;
    });
  }

  function applyLanguage(lang, shouldRemember) {
    var safeLang = normalizeLang(lang);
    var dict = I18N[safeLang] || {};

    document.documentElement.lang = safeLang;
    document.documentElement.dir = RTL[safeLang] ? "rtl" : "ltr";

    document.querySelectorAll("[data-i18n]").forEach(function (el) {
      var key = el.getAttribute("data-i18n");
      el.textContent = dict[key] || el.dataset.i18nDefault || el.textContent;
    });

    document.querySelectorAll("[data-i18n-html]").forEach(function (el) {
      var key = el.getAttribute("data-i18n-html");
      el.innerHTML = dict[key] || el.dataset.i18nDefaultHtml || el.innerHTML;
    });

    document.querySelectorAll(".language-select").forEach(function (select) {
      select.value = safeLang;
      select.setAttribute("aria-label", dict["nav.language"] || "Language");
    });

    if (shouldRemember) rememberLang(safeLang);
  }

  function setupSelectors() {
    document.querySelectorAll(".language-select").forEach(function (select) {
      if (!select.options.length) {
        Object.keys(LANGUAGES).forEach(function (lang) {
          var option = document.createElement("option");
          option.value = lang;
          option.textContent = LANGUAGES[lang];
          select.appendChild(option);
        });
      }
      select.addEventListener("change", function () {
        applyLanguage(select.value, true);
      });
    });
  }

  document.addEventListener("DOMContentLoaded", function () {
    captureDefaults();
    setupSelectors();
    applyLanguage(getInitialLang(), false);
  });
